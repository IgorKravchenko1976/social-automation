def contains_blocked_territory(text: str) -> str | None:
    """Check if text mentions any blocked territory.

    Returns the matched keyword or None if safe.
    """
    lower = text.lower()
    for kw in BLOCKED_TERRITORY_KEYWORDS:
        if kw in lower:
            return kw
    return None

